from scipy.special import comb as scipy_comb

def p_from_ansatz(params, N, q_values):
    w0_ = params['w0']
    P   = np.zeros_like(q_values, dtype=float)
    # Cap summation: f(w) is negligibly different from a beyond w_max
    w_max = min(N, w0_ + 300)
    for w in range(w0_, w_max + 1):
        fw = f_ansatz_5(
            float(w), float(params['w0']), params['f0'], params['a'],
            params['gamma1'], params['gamma2'], params['wc'],
        )
        # Log-space to avoid underflow at small q
        with np.errstate(divide='ignore', invalid='ignore'):
            log_binom = (
                np.log(scipy_comb(N, w, exact=False))
                + w * np.log(np.where(q_values > 0, q_values, 1e-300))
                + (N - w) * np.log(np.where(q_values < 1, 1 - q_values, 1e-300))
            )
        P += fw * np.exp(np.clip(log_binom, -700, 0))
    return P

if params is not None:
    q_values = np.logspace(-4, -1, 200)
    P_ansatz = p_from_ansatz(params, N, q_values)

    # Find P at q=0.001 (the operating point used to generate the trace)
    p_op  = 0.001
    idx   = np.argmin(np.abs(q_values - p_op))
    P_at_p = float(P_ansatz[idx])

    print(f"\n  P(q) at selected error rates:")
    for q_show in [1e-4, 5e-4, 1e-3, 2e-3, 5e-3, 1e-2]:
        i = np.argmin(np.abs(q_values - q_show))
        print(f"    q={q_show:.0e}  →  P={P_ansatz[i]:.4e}")

    print(f"\n  Monte Carlo LER at p=0.001 : {mc_ler:.5f}  ({mc_predicted_fails}/{n_mc} shots)")
    print(f"  Ansatz P(q=0.001)          : {P_at_p:.5f}")

    if mc_ler > 0:
        ratio = P_at_p / mc_ler
        print(f"  Ratio (ansatz / MC)        : {ratio:.3f}  (expect 0.5–2.0 at this trial count)")
        check(0.1 < ratio < 10.0,
              f"P(q) order-of-magnitude agrees with Monte Carlo LER "
              f"[ansatz={P_at_p:.4e}, MC={mc_ler:.4e}, ratio={ratio:.2f}]",
              "Large ratio is expected with low trial counts — "
              "use ≥50,000 trials/weight for quantitative agreement")
    else:
        warn(False, "Monte Carlo LER is 0 — cannot compute ratio (too few shots)")

    # Sanity checks on the P(q) curve shape
    check(np.all(P_ansatz >= 0),
          "P(q) ≥ 0 everywhere")
    check(np.all(P_ansatz <= a + 1e-6),
          f"P(q) ≤ a={a} everywhere  [max={P_ansatz.max():.5f}]")
    check(np.all(np.diff(P_ansatz) >= -1e-10),
          "P(q) is monotonically non-decreasing in q")

    # P at very low q should be much smaller than at p=0.001
    P_very_low = float(P_ansatz[np.argmin(np.abs(q_values - 1e-4))])
    check(P_very_low < P_at_p * 0.1,
          f"P(1e-4) << P(1e-3) — curve shows correct scaling "
          f"[P(1e-4)={P_very_low:.4e}, P(1e-3)={P_at_p:.4e}]")

    # P at high q should approach the asymptote
    P_high = float(P_ansatz[-1])   # at q=0.1
    warn(P_high > 0.1,
         f"P(q=0.1) = {P_high:.3f} is approaching the high-q regime")
else:
    print("  Skipping P(q) reconstruction — fit failed in Step 7")

