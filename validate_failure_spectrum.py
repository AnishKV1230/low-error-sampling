def estimate_failure_spectrum(H, A, decoder, w_range, trials_per_weight, seed=0):
    rng  = np.random.default_rng(seed)
    spec = {}
    for w in w_range:
        fails, trials = sample_fixed_weight(w, trials_per_weight, H, A, decoder, rng)
        f_hat  = fails / trials
        stderr = np.sqrt(f_hat * (1 - f_hat) / trials) if trials > 0 else 0.0
        spec[w] = (f_hat, stderr)
    return spec

