def sample_fixed_weight(w, num_trials, H, A, decoder, rng):
    n = H.shape[1]
    failures = 0
    for _ in range(num_trials):
        e = np.zeros(n, dtype=np.uint8)
        e[rng.choice(n, size=w, replace=False)] = 1
        if is_failure(e, H, A, decoder):
            failures += 1
    return failures, num_trials

