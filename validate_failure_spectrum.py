from scipy.optimize import curve_fit

def fit_ansatz(spectrum, K, w0):
    a = 1.0 - 1.0 / (2 ** K)

    # Only fit points where we observed at least 1 failure
    ws      = np.array([w for w, (f, _) in sorted(spectrum.items()) if f > 0],
                       dtype=float)
    f_vals  = np.array([spectrum[int(w)][0] for w in ws])
    f_errs  = np.array([max(spectrum[int(w)][1], 1e-15) for w in ws])

    if len(ws) < 4:
        print(f"  Only {len(ws)} non-zero spectrum points — fit may be unreliable.")
        print("  Increase trials_per_weight or extend w_range to higher weights.")

    def model(w, f0, gamma1, gamma2, wc):
        return f_ansatz_5(w, float(w0), f0, a, gamma1, gamma2, float(wc))

    # Initial guess and bounds
    f0_guess  = f_vals[0] if len(f_vals) > 0 else 1e-5
    wc_guess  = float(ws[len(ws)//2]) if len(ws) > 0 else float(w0 * 3)
    p0        = [f0_guess, float(w0) * 0.8, float(w0) * 1.2, wc_guess]
    bounds    = ([1e-15, 0.1, 0.1, float(w0)],
                 [1.0,   50., 50., float(max(ws)) * 2 if len(ws) > 0 else 500.])

    try:
        popt, pcov = curve_fit(
            model, ws, f_vals,
            p0=p0, bounds=bounds, sigma=f_errs,
            maxfev=20000,
        )
        f0_fit, g1_fit, g2_fit, wc_fit = popt
        return {
            'w0': w0, 'a': a, 'f0': f0_fit,
            'gamma1': g1_fit, 'gamma2': g2_fit, 'wc': wc_fit,
            'pcov': pcov, 'fit_weights': ws, 'fit_f': f_vals,
        }
    except Exception as e:
        print(f"  curve_fit failed: {e}")
        return None

params = fit_ansatz(spectrum, K=K, w0=w0)

